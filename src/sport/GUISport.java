    private void displayBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBTNActionPerformed
        // TODO add your handling code here:
        LinkedList<Student>  stud;
        StudentList2 studList=new StudentList2();
        

        try
        {
            stud=studList.DisplayStudentInfo();
            for(int i=0;i<stud.size();i++)
            {
                String name= stud.get(i).getName();
                String matricNo=stud.get(i).getMatricNo();
                String inasis=stud.get(i).getInasis();
                String phoneNum=stud.get(i).getPhoneNum();
                String programme=stud.get(i).getProgramme();
                String collage=stud.get(i).getCollage();
                String item=stud.get(i).getItem();
                
                receiptTA.setText(name+"\t"+matricNo+"\t"+inasis+"\t"+phoneNum+"\t"+item+"\t"+programme+"\t"+collage);
                System.out.printf("%-20s%-15s%-15s%-15s%-10s%-10s%-10s\n",stud.get(i).getName(),stud.get(i).getMatricNo(),stud.get(i).getInasis(),stud.get(i).getPhoneNum(),stud.get(i).getItem(),stud.get(i).getProgramme(),stud.get(i).getCollage());
                
                
  
            }
        }
        catch(FileNotFoundException excep)
        {
            System.out.println("File is not found");
            excep.getStackTrace();
            excep.getMessage();
        }
        catch(IOException ioEx)
        {
            ioEx.getStackTrace();
            ioEx.getMessage();
        }
    }//GEN-LAST:event_displayBTNActionPerformed

