    private void bookBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBTNActionPerformed
        // TODO add your handling code here:
        String name = nameTF.getText();
        String matricNo = matricnumTF.getText();
        String inasis = inasisTF.getText();
        String phoneNum= phoneNumTF.getText();
        String programme = programmeTF.getText();
        String collage = collageTF.getText();
        String item = itemTF.getText();

        receiptTA.setText(String.format("%-15s%-15s%-15s%-20s%-15s%-10s%-10s\n",name,matricNo,inasis,phoneNum,item,programme,collage));
        student = new Student(name,matricNo,inasis,phoneNum,item,programme,collage); // saving to array
        operation.inputStudentInfo(student);
        JOptionPane.showMessageDialog(this, "the Record has been saved Succesfully");
    }//GEN-LAST:event_bookBTNActionPerformed

